package constructor;

public class ConstructorDocs {
	/*
	 * ========================== Constructor =====================================
	 *  # use to create object in java.
	 *  # special type of method which has same name as class name.
	 *  # no return type
	 *  # if there is no any user defined constructor in class then java provides
	 *    default constructor to create object.
	 *    
	 *  # Types :
	 *    1> Default Constructor :
	 *    	-> use to create object and set default values to it.
	 *     syntax :
	 *       class_name(){
	 *       
	 *           //set default values to object
	 *       }
	 *    
	 *    2> Parameterized Constructor :
	 *    
	 *       -> use to create object and set dynamic values to it.
	 *       syntax :
	 *       class_name(arg1,arg2,arg3,....,argn){
	 *       
	 *           //set dynamic values to  object
	 *       
	 *       }
	 *     
	 *     
	 */

}
